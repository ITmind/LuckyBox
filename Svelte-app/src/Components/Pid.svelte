						<h4>Настраиваем дифференциальный коэффициент (Kd)</h4>
						<img class="img-responsive col-md-4" src="kd.jpg" align="left">
						<p class="text-justify">Постепенно увеличивая дифференциальную составляющую, необходимо добиться уменьшения или полного исчезновения «скачков» графика (перерегулирования) перед выходом на уставку. При этом кривая должна стать еще больше похожа на идеальную. Если слишком сильно завысить дифференциальный коэффициент, температура при выходе на уставку будет расти не плавно, а скачками (как показано на рисунке).</p>
						<p class="text-justify">При появлении таких скачков необходимо прекратить увеличение дифференциального коэффициента.</p>
