						<h4>Настраиваем интегральный коэффициент (Ki)</h4>
						<img class="img-responsive col-md-4" src="pids.jpg" align="left">
						<p class="text-justify">При настройке двух предыдущих коэффициентов можно получить практически идеальную кривую регулирования или близкую к ней кривую, удовлетворяющую условиям задачи. Однако, как правило возникает так называемая «статическая ошибка». При этом в нашем примере температура стабилизируется не на заданном значении 25 °С, а на несколько меньшем значении. Дело в том, что если температура станет равной уставке (то есть разность текущей и заданной температур станет равна 0), то пропорциональная и дифференциальная составляющая будут равны нулю. При этом мощность регулятора тоже станет равна 0 и он начнёт остывать.</p>
						<p class="text-justify">Для того чтобы исключить этот эффект, используют интегральную составляющую. Её необходимо постепенно увеличивать до исчезновение статической ошибки. Однако, чрезмерное её увеличение тоже может привести к возникновению скачков температуры.</p>
